# 输入整数a和b，若a*a+b*b大于等于100，则输出a*a+b*b百位及以上的数字，
# 否则输出a*a+b*b。例如，输入25 25 ，它们的平方和为625+625=1250，百位及以上数为12.
# 输入：两个整数，中间用空格隔开
# 输出：一个整数。


# 请求用户输入一系列数字，以空格分隔
numbers = input().split()

# 计算输入数字的平方和
# 'int(i)'将字符串转换为整数
# 'int(i)*int(i)'计算每个数字的平方
# 'sum(...)'计算所有平方的总和
tot = sum(int(i)*int(i) for i in numbers)

# 检查总和是否大于或等于100
if tot >= 100:
    # 如果是，打印总和除以100的结果，格式化为没有小数点的整数
    print(f"{tot/100:.0f}")
else:
    # 如果不是，直接打印总和
    print(tot)

